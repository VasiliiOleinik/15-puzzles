<?php

namespace App\Http\Admin;

use SleepingOwl\Admin\Contracts\Display\DisplayInterface;
use SleepingOwl\Admin\Contracts\Form\FormInterface;
use SleepingOwl\Admin\Section;

/**
 * Class Tag
 *
 * @property \App\Models\Tag $model
 *
 * @see http://sleepingowladmin.ru/docs/model_configuration_section
 */
class Tag extends Section
{
    /**
     * @see http://sleepingowladmin.ru/docs/model_configuration#ограничение-прав-доступа
     *
     * @var bool
     */
    protected $checkAccess = false;

    /**
     * @var string
     */
    protected $title;

    /**
     * @var string
     */
    protected $alias;

    /**
     * @return DisplayInterface
     */
    public function onDisplay()
    {
        $display = \AdminDisplay::datatablesAsync()->setColumns([
            \AdminColumn::text('tagRu.name')->setLabel('Название тега'),
        ]);
        $display->setApply(function ($query) {
            $query->where('language', 'ru');
        });
        return $display;
    }

    /**
     * @param int $id
     *
     * @return FormInterface
     */
    public function onEdit($id)
    {
        $columns1 = \AdminFormElement::columns([
            [
                \AdminFormElement::text('tagEng.name')->setLabel('Название тега ENG'),
            ],
            [
                \AdminFormElement::text('tagRu.name')->setLabel('Название тега RU'),

                \AdminFormElement::hidden('tagRu.language')->setDefaultValue('ru'),
                \AdminFormElement::hidden('tagEng.language')->setDefaultValue('eng')
            ]
        ]);


        $form = \AdminForm::panel()->addBody([
            $columns1,
        ]);
        return $form;
    }

    /**
     * @return FormInterface
     */
    public function onCreate()
    {
        return $this->onEdit(null);
    }

    /**
     * @return void
     */
    public function onDelete($id)
    {
        // remove if unused
    }

    /**
     * @return void
     */
    public function onRestore($id)
    {
        // remove if unused
    }
}
